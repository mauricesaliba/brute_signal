package mt.com.go.apoe.model;

public class CellPosition {

    private int row;
    private int column;

    public CellPosition(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }

}
